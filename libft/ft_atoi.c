#include "libft.h"

int				ft_atoi(const char *str)
{
	long long		res;
	long long		sign;
	long int		i;

	res = 0;
	sign = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
			str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (ft_strlen(str) == 26 && sign == 1)
			return (-1);
		if (ft_strlen(str) == 27 && sign == -1)
			return (0);
		res = res * 10 + str[i++] - '0';
	}
	return ((res * sign));
}
